public class Array {
    public static void main(String[] args) {
        // Deklarasi array dengan panjang tetap
        String[] nama = {"Ani", "Budi", "Citra"};

        // Menampilkan isi array
        for (int i = 0; i < nama.length; i++) {
            System.out.println("Nama ke-" + i + ": " + nama[i]);
        }
    }
}
